    private void ypdateB1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ypdateB1ActionPerformed

        // TODO add your handling code here:
        sidCombo.removeAllItems();
        String[] data =null;
        int[] id = null;
        try {
            id = stu.loadStudentId();
        } catch (SQLException ex) {
            Logger.getLogger(Home.class.getName()).log(Level.SEVERE, null, ex);
        }

        for (int i = 0; i < id.length; i++) {
            if (id[i] != 0) {
                sidCombo.enable();
                sidCombo.addItem(Integer.toString(id[i]));
            }
        }
        
                
        int stuId = Integer.parseInt(sidCombo.getSelectedItem().toString());
        
        try {
           data = stu.getUserInfo(stuId);
        } catch (SQLException ex) {
            Logger.getLogger(Home.class.getName()).log(Level.SEVERE, null, ex);
        }
        String uname = data[2]; 
        
        if (!addT2.getText().equals("") && !fnameT3.getText().equals("") &&
               !lnameT3.getText().equals("") && !mnameT2.getText().equals("") && !telT2.getText().equals("") && 
                       !batchT2.getText().equals("") && !dobT2.getDate().equals("") && !dateT2.getDate().equals("")) {
                       if(telT2.getText().length()==10){
            
                    try {
                        Date dob = dobT2.getDate();
                        Date regD = dateT2.getDate();
                        SimpleDateFormat f = new SimpleDateFormat("yyyy-MM-dd");
                        String dateob = f.format(dob);
                        String datereg = f.format(regD);
                        stu.updateUser(fnameT3.getText(),lnameT3.getText(), uname);
                        stu.updateUser(mnameT2.getText(),dateob,addT2.getText(),telT2.getText(),
                                Integer.parseInt(batchT2.getText()),datereg,uname);
                        JOptionPane.showMessageDialog(this, "Update successful");
                    } catch (SQLException ex) {
                        Logger.getLogger(Home.class.getName()).log(Level.SEVERE, null, ex);
                    }

                       }else
                       JOptionPane.showMessageDialog(this, "Incorrect phone number ");    
        } else {
            JOptionPane.showMessageDialog(this, "Fill all the details ");
        }
    }//GEN-LAST:event_ypdateB1ActionPerformed

