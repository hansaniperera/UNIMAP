    private void loadBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loadBActionPerformed

        // TODO add your handling code here:
        JFileChooser chooser = new JFileChooser();
        chooser.setFileSelectionMode(JFileChooser.FILES_ONLY);

        chooser.addChoosableFileFilter(new FileNameExtensionFilter("Images", "jpg", "png", "gif", "bmp"));
        chooser.setAcceptAllFileFilterUsed(true);
        chooser.showOpenDialog(null);
        file = chooser.getSelectedFile();

        try {
            if (!file.getAbsolutePath().equals(null)) {
                path = file.getAbsolutePath();
                Image image = ImageIO.read(file).getScaledInstance(153, 153, Image.SCALE_DEFAULT);
                ImageIcon icon = new ImageIcon(image);
                imageL.setIcon(icon);
            } else {
                path = null;
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Image not selected");
        }


    }//GEN-LAST:event_loadBActionPerformed

